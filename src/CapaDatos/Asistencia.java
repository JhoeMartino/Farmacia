package CapaDatos;

public class Asistencia {

    private int idasistencia;
    private String aFechaE;
    private String aHoraE;
    private String aHorasS;
    private String AuDni;
    private String aTurno;
    private String aEstado;
    private String tienda;

    public Asistencia() {
    }

    public int getIdasistencia() {
        return idasistencia;
    }

    public void setIdasistencia(int idasistencia) {
        this.idasistencia = idasistencia;
    }

    public String getaFechaE() {
        return aFechaE;
    }

    public void setaFechaE(String aFechaE) {
        this.aFechaE = aFechaE;
    }

    public String getaHoraE() {
        return aHoraE;
    }

    public void setaHoraE(String aHoraE) {
        this.aHoraE = aHoraE;
    }

    public String getaHorasS() {
        return aHorasS;
    }

    public void setaHorasS(String aHorasS) {
        this.aHorasS = aHorasS;
    }

    public String getAuDni() {
        return AuDni;
    }

    public void setAuDni(String AuDni) {
        this.AuDni = AuDni;
    }

    public String getaTurno() {
        return aTurno;
    }

    public void setaTurno(String aTurno) {
        this.aTurno = aTurno;
    }

    public String getaEstado() {
        return aEstado;
    }

    public void setaEstado(String aEstado) {
        this.aEstado = aEstado;
    }

    public String getTienda() {
        return tienda;
    }

    public void setTienda(String tienda) {
        this.tienda = tienda;
    }

    
}
